/**
 * Synchronous SHA-256.
 *
 * Why not Web Crypto: `crypto.subtle.digest` is async, and the ledger is built
 * synchronously at module load (like every other mock generator here) so the
 * first paint is deterministic and SSR-safe. An async chain would force the
 * whole ledger behind a loading state for no benefit.
 *
 * This is a straight FIPS 180-4 implementation — the digests are real, so the
 * chain in `ledger.ts` genuinely verifies and genuinely breaks when tampered
 * with. That is the point of the demo.
 */

